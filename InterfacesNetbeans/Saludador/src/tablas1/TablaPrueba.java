    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSel = tabla.getSelectedRow();
        if(filaSel==-1){
            JOptionPane.showMessageDialog(null, "No hay ninguna fila seleccionada");
        }else{
            m=(DefaultTableModel) tabla.getModel();
            m.removeRow(filaSel);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnVerValorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerValorActionPerformed
       int fila;
       int columna;
       String valor;
       
        try {
            fila=Integer.parseInt(txtFila.getText());
            columna=Integer.parseInt(txtColumna.getText());
            m=(DefaultTableModel) tabla.getModel();
            valor=(String)m.getValueAt(fila, columna);
            etiValor.setText(valor);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recoger el valor");
        }
    }//GEN-LAST:event_btnVerValorActionPerformed

    private void btnIntroducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntroducirActionPerformed
        int fila;
       int columna;
       String valor;
       
        try {
            fila=Integer.parseInt(txtFila.getText());
            columna=Integer.parseInt(txtColumna.getText());
            m=(DefaultTableModel) tabla.getModel();
            valor=(String)txtNuevoValor.getText();
            tabla.setValueAt(valor, fila, columna);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recoger el valor");
        }
    }//GEN-LAST:event_btnIntroducirActionPerformed

